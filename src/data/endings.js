  getJokeEnding(id) {
    return this.jokeEndings[id] || null;
  },

  getPropertyEnding(id) {
    return this.propertyEndings.find(e => e.id === id) || null;
  },

  getCareerEnding(id) {
    return this.careerEndings.find(e => e.id === id) || null;
  },

  /** 根据属性判定属性结局 */
  judge(attrs) {
    // 按优先级从高到低检查
    if (attrs.体力 < 15) {
      return this.getPropertyEnding('overwork');
    }
    if (attrs.专业度 < 25 || attrs.人缘 < 20) {
      return this.getPropertyEnding('medical_affairs');
    }
    if (attrs.人缘 >= 65 && attrs.专业度 >= 45) {
      return this.getPropertyEnding('best_partner');
    }
    if (attrs.专业度 >= 75 && attrs.人缘 >= 50) {
      return this.getPropertyEnding('er_star');
    }
    return this.getPropertyEnding('barely_pass');
  },
